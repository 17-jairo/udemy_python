# Tipo Float (Tipo real, decimal)
# Casas decimais
# OBS: Os separadores de casas na programação é o posto e não a virgula

#Errado
valor = 1,44
print(valor)
print(type(valor))
#Certo
valor = 1.44
print(valor)
print(type(valor))

# Podemos converter um float para um int
res = int(valor)
print(res)
print(type(res))

